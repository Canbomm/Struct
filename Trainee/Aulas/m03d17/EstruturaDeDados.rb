# Arrays
# a lista pode ter varios tipos de dados
listateste = ["Palavra",0,0.5]
p listateste

# push adiciona um elemento no final do array
listateste.push("Coca-Cola")
p listateste

# insert insere na posicao que voce quiser
listateste.insert(0,"Oi eu to no comeco")
p listateste

# retirar elemento
# retira o ultimo elemento, pode atribuir a uma variavel
listateste.pop
# retira o primeiro elemento, tambem pode atribuir
listateste.shift
# retira um elemento especifico
listateste.delete_at(4)
